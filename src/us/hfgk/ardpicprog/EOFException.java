package us.hfgk.ardpicprog;


class EOFException extends ProgrammerException {

	private static final long serialVersionUID = 1L;

	EOFException() {
		super();

	}

	EOFException(String message, Throwable cause) {
		super(message, cause);

	}

	EOFException(String message) {
		super(message);

	}

	EOFException(Throwable cause) {
		super(cause);

	}

}